def to_fullwidth(text: str) -> str:
    """
    Convert printable ASCII characters (33..126) to their fullwidth Unicode equivalents.
    Spaces are kept as normal spaces for readability.
    """
    out = []
    for ch in text:
        code = ord(ch)
        if 33 <= code <= 126:
            out.append(chr(code + 0xFEE0))
        else:
            out.append(ch)
    return "".join(out)

